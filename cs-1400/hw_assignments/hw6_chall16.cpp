/* Von Reid CS-1400, Chapter 2 Challenge 16 (HWK #6)
Write a program that will display a diamond pattern, like this:
      *
     ***
    *****
   *******
    *****
     ***
      *
[end of pattern] */

#include <iostream>

int main() {
    std::cout << "      *\n";
    std::cout << "     ***\n";
    std::cout << "    *****\n";
    std::cout << "   *******\n";
    std::cout << "    *****\n";
    std::cout << "     ***\n";
    std::cout << "      *\n";
    return 0;
}